package helper;

public class User {
    public static String username;
    public static String password;
    public static int userID;

    public User() {}

    /**
     *
     * @return username
     */
    //may be able to be deleted
    public String getUsername() {
        return username;
    }

    /**
     *
     * @return userID
     */
    public int getUserID() {
        return userID;
    }

    /**
     *
     * @return password
     */
    //may be able to be deleted
    public String getPassword() {
        return password;
    }
}
